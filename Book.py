class Book:

        
    def getISBN(self):
        return self.isbn

    def setISBN(self, isbn):
        self.isbn = isbn

    def getBook(self):
        return self.book

    def setBook(self, book):
        self.book = book

    def getAuthor(self):
        return self.author

    def setAuthor(self, author):
        self.author = author

    def getPublishYear(self):
        return self.year

    def setPublishYear(self, year):
        self.year = year

    def getPublisher(self):
        return self.publisher

    def setPublisher(self, publisher):
        self.publisher = publisher

    